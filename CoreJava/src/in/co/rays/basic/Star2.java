package in.co.rays.basic;

public class Star2 { 
	
	public static void main(String args []) {
		
		int p=10;
		 
		//OUTER LOOP WILL PRINT ROWS
		for(int i=1;i<=p;i++) {
			//INNER LOOP WILL PRINT COLOUM
			for(int j=1;j<=i;j++) {
				
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
	}

}
