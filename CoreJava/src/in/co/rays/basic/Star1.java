package in.co.rays.basic;

public class Star1 { 
	
	
	public static void main(String[]args) { 
		
		
		int n=4;
		int m=5; 
		
		//OUTER LOOP OR ROWS
		for(int i=1;i<=n;i++) { 
			//INNER LOOP OR COLOUM
			for (int j=1;j<=m;j++) { 
				
				System.out.print("*");
				
			} 
			System.out.println();
			
			
		}
	}

}
